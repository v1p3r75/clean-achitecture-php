<?php

namespace Application\Response;

use Domain\Entity\User;

class UserResponse extends BaseResponse
{

    private ?User $user = null;

    /**
     * @return User|null
     */
    public function getUser(): ?User
    {
        return $this->user;
    }

    /**
     * @param User|null $user
     */
    public function setUser(?User $user): void
    {
        $this->user = $user;
    }

}